export const toggleCheckBox = (type, value, setCheckedFilters) => {
  setCheckedFilters((prev) => ({
    ...prev,
    [type]:
      prev[type].includes(value) ?
        prev[type].filter((el) => el !== value)
      : [...prev[type], value],
  }));
};
